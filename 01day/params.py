# 使用变量保存数据，并对数据进行简单算法

a = 45  # 变量a保存了45
b = 12  # 变量b保存了12
print(a + b)  # 57
print(a - b)  # 33
print(a * b)  # 540
print(a / b)  # 3.75


# 使用type()检查变量的类型

a = 100
b = 12.345
c = 'hello, world'
d = True
print(type(a))  # <class 'int'>
print(type(b))  # <class 'float'>
print(type(c))  # <class 'str'>
print(type(d))  # <class 'bool'>
